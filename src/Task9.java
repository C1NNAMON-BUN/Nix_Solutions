public class Task9 {
    void Method_task_9() {
        int number = (int) (Math.random() * (10 - 1)) + 1;
        if (number == 1) {
            System.out.print("Понедельник");
        } else if (number == 2) {
            System.out.print("Вторник");
        } else if (number == 3) {
            System.out.print("Среда");
        } else if (number == 4) {
            System.out.print("Четверг");
        } else if (number == 5) {
            System.out.print("Пятница");
        } else if (number == 6) {
            System.out.print("Суббота");
        } else if (number == 7) {
            System.out.print("Воскресенье");
        }
    }
}
